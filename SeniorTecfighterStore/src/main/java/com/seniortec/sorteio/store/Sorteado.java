package com.seniortec.sorteio.store;

public class Sorteado {

	private String nome;
	private String username;
	private String palestra;
	private String brinde;

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getPalestra() {
		return palestra;
	}

	public void setPalestra(String palestra) {
		this.palestra = palestra;
	}

	public String getBrinde() {
		return brinde;
	}

	public void setBrinde(String brinde) {
		this.brinde = brinde;
	}

}
